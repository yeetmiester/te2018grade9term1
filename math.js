// Variables
let num1 = 5;
let num2 = 10;

// **** Problem 1 ****
// Store the sum of num1 and num2 in a variable.
// Print the sum.
let sum = num1 + num2;
console.log(sum);

// **** Problem 2 ****
// Store the difference between sum (above) and 7 in a variable.
// Print the difference.
let num3 = 7;
let difference = sum - num3;
console.log(difference);

// **** Problem 3 ****
// Store the product of difference and 3 in a variable.
// Print the product.
let num4 = 3;
let product = difference * num4;
console.log(product);

// **** Problem 4 ****
// Store the power of the product raised to an exponent of 2 in a variable.
// Print the power.
let num5 = 2;
let power = product ** num5;
console.log(power);

// **** Problem 5 ****
// Store the quotient of the power divided by 4 in a variable.
// Print the quotient.
let num6 = 4;
let quotient = power / num6;
console.log(quotient);

// **** Problem 6 ****
// Store the remainder of the quotient divided by 2 in a variable.
// Print the remainder.
let num7 = 2;
let remainder = quotient % num7;
console.log(remainder);

